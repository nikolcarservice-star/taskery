export type ActionErrorCode =
  (typeof ActionErrorCode)[keyof typeof ActionErrorCode];

const ACTION_ERROR_CODES = new Set<string>(Object.values(ActionErrorCode));

export function isActionErrorCode(value: string): value is ActionErrorCode {
  return ACTION_ERROR_CODES.has(value);
}

export function actionError(code: ActionErrorCode): { error: ActionErrorCode } {
  return { error: code };
}
